package ex_18042024;

public class lab003 {
    public static void main(String[] args) {
        System.out.println('A' == 65); // Follow the ASCII rule here
        System.out.println('C' == 67);
        System.out.println('p' == 112);
        System.out.println('p' == 111);

        // Logical Operator
        // ! reverse the answer
        System.out.println(!(10>20));
        System.out.println(!!(10>20)); // We can keep using nagation
        System.out.println(!!!(10>20));


    }
}
